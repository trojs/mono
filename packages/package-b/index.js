/**
 * Returns a greeting message addressed to the specified name.
 *
 * @param {string} name - The name to include in the greeting.
 * @returns {string} A greeting string in the format "Hello, {name}!".
 */
export function hello (name) {
  return `Hello, ${name}!`
}

/**
 * Returns the sum of two numbers.
 *
 * @param {number} a - The first addend.
 * @param {number} b - The second addend.
 * @returns {number} The result of adding {@link a} and {@link b}.
 */
export function add (a, b) {
  return a + b
}
